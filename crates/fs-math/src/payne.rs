/// Payne–Hanek reduction of any finite x with |x| > 1: returns the
/// reduced argument r ∈ [−π/4, π/4] (as the rounded double of a
/// double-double product) and the quadrant of x modulo 2π.
///
/// # Panics
/// On non-finite input (callers gate NaN/∞ first — det.rs contract).
#[must_use]
pub fn reduce_pio2_large(x: f64) -> (f64, u8) {
    assert!(x.is_finite(), "Payne-Hanek requires finite input");
    let neg = x < 0.0;
    let bits = x.abs().to_bits();
    let e = i64::try_from((bits >> 52) & 0x7FF).expect("11-bit field") - 1023;
    let m = (bits & ((1u64 << 52) - 1)) | (1u64 << 52); // 53-bit mantissa
    let s = e - 52; // x = m · 2^s
    // 256-bit accumulator, little-endian limbs; bit index i (0..256)
    // carries weight 2^(i − 254): bits 255..254 are the quadrant
    // (weights 2^1, 2^0), the rest the fraction of (x·2/π) mod 4.
    let mut acc = [0u64; 4];
    for (i, &limb) in TWO_OVER_PI_LIMBS.iter().enumerate() {
        // Term m·L_i with LSB weight 2^(s − 64(i+1)) → LSB accumulator
        // bit index:
        let idx = s - 64 * (i64::try_from(i).expect("small index") + 1) + 254;
        if idx >= 256 {
            continue; // whole multiple of 4 quadrants — drops out mod 4
        }
        if idx <= -118 {
            continue; // entirely below the accumulator floor
        }
        let prod = u128::from(m) * u128::from(limb);
        add_shifted(&mut acc, prod, idx);
    }
    // Quadrant bits and 254-bit fraction.
    let mut q = (acc[3] >> 62) as u8 & 3;
    // Fraction as the top 128 bits after the quadrant (weights 2^-1 …).
    let f_hi = (acc[3] << 2) | (acc[2] >> 62);
    let f_lo = (acc[2] << 2) | (acc[1] >> 62);
    let (frac_hi, frac_lo, negate) = if f_hi >> 63 == 1 {
        // f ≥ ½: round the quadrant up, take the negative complement
        // (r = (f − 1)·π/2 ∈ [−π/4, 0)).
        q = (q + 1) & 3;
        let lo = (!f_lo).wrapping_add(1);
        let hi = if lo == 0 {
            (!f_hi).wrapping_add(1)
        } else {
            !f_hi
        };
        (hi, lo, true)
    } else {
        (f_hi, f_lo, false)
    };
    // f as a double-double via EXACT 53-bit splits (a raw u64→f64
    // cast would round away 11 low bits that the lo limb cannot
    // recover): a·2^−53 + b·2^−106, both parts exact.
    let a = frac_hi >> 11;
    let b = ((frac_hi & 0x7FF) << 42) | (frac_lo >> 22);
    #[allow(clippy::cast_precision_loss)] // both fit in 53 bits — exact
    let fd = Dd::from_pair(
        (a as f64) * 2.0f64.powi(-53),  // det-ok: base 2, exact (4xnt)
        (b as f64) * 2.0f64.powi(-106), // det-ok: base 2, exact (4xnt)
    );
    let r = fd * PIO2_DD;
    let r_signed = if negate { -r.to_f64() } else { r.to_f64() };
    if neg {
        // Odd symmetry of the reduction: −x·(2/π) = −(4k + q + φ),
        // so the quadrant mirrors mod 4 and the remainder negates.
        (((4 - q) & 3, -r_signed).1, (4 - q) & 3)
    } else {
        (r_signed, q)
    }
}

/// Add `prod` (128 bits) shifted so its LSB sits at accumulator bit
/// `idx` (may be negative: low bits truncate; high bits past 255
/// wrap off — mod-4 semantics).
fn add_shifted(acc: &mut [u64; 4], prod: u128, idx: i64) {
    // Normalize to a non-negative shift by pre-truncating low bits.
    let (val, base) = if idx < 0 {
        let sh = u32::try_from(-idx).expect("bounded shift");
        if sh >= 128 {
            return;
        }
        (prod >> sh, 0u32)
    } else {
        (prod, u32::try_from(idx).expect("bounded index"))
    };
    if val == 0 {
        return;
    }
    let limb = (base / 64) as usize;
    let shift = base % 64;
    // val << shift as a 192-bit value: low 128 bits plus the overflow
    // word (val's top `shift` bits).
    let low128 = val << shift;
    let over = if shift == 0 {
        0u64
    } else {
        (val >> (128 - shift)) as u64
    };
    let parts = [low128 as u64, (low128 >> 64) as u64, over];
    let mut carry = 0u64;
    for (k, &p) in parts.iter().enumerate() {
        let t = limb + k;
        if t >= 4 {
            break;
        }
        let (s1, c1) = acc[t].overflowing_add(p);
        let (s2, c2) = s1.overflowing_add(carry);
        acc[t] = s2;
        carry = u64::from(c1) + u64::from(c2);
    }
    let mut t = limb + 3;
    while carry > 0 && t < 4 {
        let (s, c) = acc[t].overflowing_add(carry);
        acc[t] = s;
        carry = u64::from(c);
        t += 1;
    }
}
