//! Payne–Hanek argument reduction (bead r6r5): extends sin/cos/tan
//! beyond `TRIG_DOMAIN` = 2²⁰ to ALL finite f64. The engine is 1280
//! bits of 2/π stored as u64 limbs; the reduction multiplies the
//! argument's mantissa against a windowed slice of those limbs with
//! exact u64 widening arithmetic, keeps the quadrant bits and 254
//! fraction bits (mod-4 wrapping — whole multiples of 4 quadrants
//! cannot matter), and converts the fraction to a double-double times
//! π/2 for the existing polynomial cores.
//!
//! SELF-VERIFYING CONSTANTS: the limbs are hardcoded AND regenerated
//! at test time by an all-integer Machin bignum (π = 16·atan(1/5) −
//! 4·atan(1/239) in u64-limb fixed point, then binary long division
//! for 2/π) — no hand transcription is trusted; the regeneration test
//! recomputes every limb from integer arithmetic (G5: bit-identical
//! on every ISA by construction).

use crate::dd::Dd;

/// Number of shipped 2/π fraction limbs (1280 bits).
pub const PH_LIMBS: usize = 20;

/// 2/π as pure-fraction u64 limbs: 2/π = Σ LIMBS[i]·2^(−64(i+1)).
/// Generated by [`generate_two_over_pi`]; the battery regenerates and
/// compares every limb.
pub const TWO_OVER_PI_LIMBS: [u64; PH_LIMBS] = [
    0xA2F9_836E_4E44_1529,
    0xFC27_57D1_F534_DDC0,
    0xDB62_9599_3C43_9041,
    0xFE51_63AB_DEBB_C561,
    0xB724_6E3A_424D_D2E0,
    0x0649_2EEA_09D1_921C,
    0xFE1D_EB1C_B129_A73E,
    0xE882_35F5_2EBB_4484,
    0xE99C_7026_B45F_7E41,
    0x3991_D639_8353_39F4,
    0x9C84_5F8B_BDF9_283B,
    0x1FF8_97FF_DE05_980F,
    0xEF2F_118B_5A0A_6D1F,
    0x6D36_7ECF_27CB_09B7,
    0x4F46_3F66_9E5F_EA2D,
    0x7527_BAC7_EBE5_F17B,
    0x3D07_39F7_8A52_92EA,
    0x6BFB_5FB1_1F8D_5D08,
    0x5603_3046_FC7B_6BAB,
    0xF0CF_BC20_9AF4_361D,
];

/// π/2 as a double-double (hi is the correctly rounded f64; lo the
/// residual — carries the value to ~2^−107).
const PIO2_DD: Dd = Dd {
    hi: std::f64::consts::FRAC_PI_2,
    lo: 6.123_233_995_736_766e-17,
};

// ---------------------------------------------------------------------
// All-integer fixed-point bignum (generator + reference machinery).
// Value = int + Σ frac[i]·2^(−64(i+1)). Deterministic by construction.
// ---------------------------------------------------------------------

/// Fixed-point non-negative bignum for the constant generator.
#[derive(Clone, PartialEq, Eq, Debug)]
pub struct Fx {
    /// Integer part.
    pub int: u64,
    /// Fraction limbs, most significant first.
    pub frac: Vec<u64>,
}

impl Fx {
    /// Zero with `n` fraction limbs.
    #[must_use]
    pub fn zero(n: usize) -> Fx {
        Fx {
            int: 0,
            frac: vec![0; n],
        }
    }

    /// Divide in place by a small integer (school division, high to
    /// low, u128 intermediates — exact truncation toward zero).
    pub fn div_small(&mut self, d: u64) {
        let mut rem: u64 = self.int % d;
        self.int /= d;
        for limb in &mut self.frac {
            let acc = (u128::from(rem) << 64) | u128::from(*limb);
            *limb = (acc / u128::from(d)) as u64;
            rem = (acc % u128::from(d)) as u64;
        }
    }

    /// Multiply in place by a small integer (low to high with carry).
    ///
    /// # Panics
    /// If the integer part overflows u64 (generator contract — the
    /// constants involved stay tiny).
    pub fn mul_small(&mut self, m: u64) {
        let mut carry: u64 = 0;
        for limb in self.frac.iter_mut().rev() {
            let acc = u128::from(*limb) * u128::from(m) + u128::from(carry);
            *limb = acc as u64;
            carry = (acc >> 64) as u64;
        }
        let acc = u128::from(self.int) * u128::from(m) + u128::from(carry);
        assert!(acc >> 64 == 0, "Fx integer overflow in mul_small");
        self.int = acc as u64;
    }

    /// self += o (matching limb counts).
    pub fn add_assign(&mut self, o: &Fx) {
        let mut carry: u64 = 0;
        for (a, b) in self.frac.iter_mut().rev().zip(o.frac.iter().rev()) {
            let acc = u128::from(*a) + u128::from(*b) + u128::from(carry);
            *a = acc as u64;
            carry = (acc >> 64) as u64;
        }
        self.int = self.int + o.int + carry;
    }

    /// self −= o (requires self ≥ o; matching limb counts).
    pub fn sub_assign(&mut self, o: &Fx) {
        let mut borrow: u64 = 0;
        for (a, b) in self.frac.iter_mut().rev().zip(o.frac.iter().rev()) {
            let (d1, b1) = a.overflowing_sub(*b);
            let (d2, b2) = d1.overflowing_sub(borrow);
            *a = d2;
            borrow = u64::from(b1) + u64::from(b2);
        }
        self.int = self.int - o.int - borrow;
    }

    /// self ≥ o?
    #[must_use]
    pub fn ge(&self, o: &Fx) -> bool {
        if self.int != o.int {
            return self.int > o.int;
        }
        for (a, b) in self.frac.iter().zip(&o.frac) {
            if a != b {
                return a > b;
            }
        }
        true
    }

    /// All limbs zero?
    #[must_use]
    pub fn is_zero(&self) -> bool {
        self.int == 0 && self.frac.iter().all(|&l| l == 0)
    }
}

/// atan(1/n) by the alternating Gregory series, all-integer.
#[must_use]
pub fn atan_recip(n: u64, limbs: usize) -> Fx {
    let mut term = Fx::zero(limbs);
    term.int = 1;
    term.div_small(n); // 1/n
    let mut sum = term.clone();
    let n2 = n * n;
    let mut k: u64 = 1;
    let mut power = term;
    loop {
        power.div_small(n2);
        if power.is_zero() {
            break;
        }
        let mut t = power.clone();
        t.div_small(2 * k + 1);
        if k.is_multiple_of(2) {
            sum.add_assign(&t);
        } else {
            sum.sub_assign(&t);
        }
        k += 1;
    }
    sum
}

/// π by Machin's formula, all-integer.
#[must_use]
pub fn generate_pi(limbs: usize) -> Fx {
    let mut a = atan_recip(5, limbs);
    a.mul_small(16);
    let mut b = atan_recip(239, limbs);
    b.mul_small(4);
    a.sub_assign(&b);
    a
}

/// 2/π limbs by binary long division against the Machin π
/// (guard limbs beyond `out_limbs` make the shipped limbs exact).
#[must_use]
pub fn generate_two_over_pi(out_limbs: usize) -> Vec<u64> {
    let guard = out_limbs + 4;
    let pi = generate_pi(guard);
    let mut rem = Fx::zero(guard);
    rem.int = 2;
    let mut out = vec![0u64; out_limbs];
    for limb_out in &mut out {
        let mut word: u64 = 0;
        for _ in 0..64 {
            rem.mul_small(2);
            word <<= 1;
            if rem.ge(&pi) {
                rem.sub_assign(&pi);
                word |= 1;
            }
        }
        *limb_out = word;
    }
    out
}

